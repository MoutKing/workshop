public class MainProcessSite {
	
	private Integer mainProcessSiteId;
	private String  mainProcessSiteName;
	private Integer measureSiteId;
	
	public Integer getMainProcessSiteId() {
		return mainProcessSiteId;
	}
	public void setMainProcessSiteId(Integer mainProcessSiteId) {
		this.mainProcessSiteId = mainProcessSiteId;
	}
	public String getMainProcessSiteName() {
		return mainProcessSiteName;
	}
	public void setMainProcessSiteName(String mainProcessSiteName) {
		this.mainProcessSiteName = mainProcessSiteName;
	}
	public Integer getMeasureSiteId() {
		return measureSiteId;
	}
	public void setMeasureSiteId(Integer measureSiteId) {
		this.measureSiteId = measureSiteId;
	}

}
