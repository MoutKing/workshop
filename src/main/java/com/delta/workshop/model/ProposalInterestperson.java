package com.delta.workshop.model;

public class ProposalInterestperson {
	private Integer interestpersonId;
	private String interestpersonName;
	private Integer status;
	
	public Integer getInterestpersonId() {
		return interestpersonId;
	}
	public void setInterestpersonId(Integer interestpersonId) {
		this.interestpersonId = interestpersonId;
	}
	public String getInterestpersonName() {
		return interestpersonName;
	}
	public void setInterestpersonName(String interestpersonName) {
		this.interestpersonName = interestpersonName;
	}
	public Integer getStatus() {
		return status;
	}
	public void setStatus(Integer status) {
		this.status = status;
	}
	

	

}
