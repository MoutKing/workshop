package com.delta.workshop.model;

public class ProposalQualitativeBenefitAnalysis {

	private Integer qualitativeBenefitId;
	private String qualitativeBenefitItem;
	private Integer score;

	public Integer getQualitativeBenefitId() {
		return qualitativeBenefitId;
	}

	public void setQualitativeBenefitId(Integer qualitativeBenefitId) {
		this.qualitativeBenefitId = qualitativeBenefitId;
	}

	public String getQualitativeBenefitItem() {
		return qualitativeBenefitItem;
	}

	public void setQualitativeBenefitItem(String qualitativeBenefitItem) {
		this.qualitativeBenefitItem = qualitativeBenefitItem;
	}

	public Integer getScore() {
		return score;
	}

	public void setScore(Integer score) {
		this.score = score;
	}

}
